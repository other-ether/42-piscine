int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (n > 0 && (s1[i] != '\0' || s2[i] != '\0'))
	{
		if (s1[i] == s2[i])
		{
			i++;
			n--;
		}
		else
			return ((s1[i] - s2[i]));
	}
	if (n == 0)
		return (0);
	return ((s1[i] - s2[i]));
}
